'use client';

import { useSession, signOut } from 'next-auth/react';
import { useRouter } from 'next/navigation';
import Link from 'next/link';
import { useState } from 'react';

interface DashboardLayoutProps {
  children: React.ReactNode;
  role: 'TEACHER' | 'STUDENT';
}

export default function DashboardLayout({ children, role }: DashboardLayoutProps) {
  const { data: session, status } = useSession();
  const router = useRouter();
  const [sidebarOpen, setSidebarOpen] = useState(false);

  const handleLogout = async () => {
    await signOut({ redirect: false });
    router.push('/');
  };

  const teacherNavigation = [
    { name: 'Dashboard', href: '/teacher/dashboard', icon: HomeIcon },
    { name: 'Courses', href: '/teacher/courses', icon: BookIcon },
    { name: 'Students', href: '/teacher/students', icon: UsersIcon },
    { name: 'Sessions', href: '/teacher/sessions', icon: CalendarIcon },
    { name: 'Assignments', href: '/teacher/assignments', icon: ClipboardIcon },
    { name: 'Resources', href: '/teacher/resources', icon: FolderIcon },
    { name: 'Messages', href: '/teacher/messages', icon: MessageIcon },
    { name: 'Settings', href: '/teacher/settings', icon: SettingsIcon },
  ];

  const studentNavigation = [
    { name: 'Dashboard', href: '/student/dashboard', icon: HomeIcon },
    { name: 'My Courses', href: '/student/courses', icon: BookIcon },
    { name: 'Schedule', href: '/student/schedule', icon: CalendarIcon },
    { name: 'Assignments', href: '/student/assignments', icon: ClipboardIcon },
    { name: 'Resources', href: '/student/resources', icon: FolderIcon },
    { name: 'Community', href: '/student/community', icon: UsersIcon },
    { name: 'Messages', href: '/student/messages', icon: MessageIcon },
    { name: 'Profile', href: '/student/profile', icon: UserIcon },
  ];

  const navigation = role === 'TEACHER' ? teacherNavigation : studentNavigation;
  const userName = session?.user?.name || session?.user?.email || 'User';
  const dashboardTitle = role === 'TEACHER' ? 'Teacher Portal' : 'Student Portal';

  if (status === 'loading') {
    return (
      <div className="min-h-screen flex items-center justify-center">
        <div className="text-lg">Loading...</div>
      </div>
    );
  }

  return (
    <div className="min-h-screen bg-gray-50">
      {/* Mobile sidebar backdrop */}
      {sidebarOpen && (
        <div
          className="fixed inset-0 bg-gray-900 bg-opacity-50 z-40 lg:hidden"
          onClick={() => setSidebarOpen(false)}
        />
      )}

      {/* Sidebar */}
      <div className={`fixed inset-y-0 left-0 z-50 w-64 bg-white shadow-lg transform transition-transform duration-300 lg:translate-x-0 lg:static lg:inset-0 ${
        sidebarOpen ? 'translate-x-0' : '-translate-x-full'
      }`}>
        <div className="flex flex-col h-full">
          {/* Logo */}
          <div className="flex items-center justify-between h-16 px-4 border-b">
            <h2 className="text-xl font-bold text-trc-gray-900">{dashboardTitle}</h2>
            <button
              className="lg:hidden"
              onClick={() => setSidebarOpen(false)}
            >
              <XIcon className="w-6 h-6" />
            </button>
          </div>

          {/* Navigation */}
          <nav className="flex-1 px-4 py-4 space-y-1 overflow-y-auto">
            {navigation.map((item) => (
              <Link
                key={item.name}
                href={item.href}
                className="flex items-center px-3 py-2 text-sm font-medium rounded-md hover:bg-trc-blue-50 hover:text-trc-blue-600 transition-colors"
              >
                <item.icon className="w-5 h-5 mr-3" />
                {item.name}
              </Link>
            ))}
          </nav>

          {/* User section */}
          <div className="p-4 border-t">
            <div className="flex items-center">
              <div className="flex-shrink-0">
                <div className="w-10 h-10 bg-trc-blue-600 rounded-full flex items-center justify-center">
                  <span className="text-white font-semibold">
                    {userName.charAt(0).toUpperCase()}
                  </span>
                </div>
              </div>
              <div className="ml-3 flex-1">
                <p className="text-sm font-medium text-trc-gray-900">{userName}</p>
                <p className="text-xs text-trc-gray-600">{role}</p>
              </div>
              <button
                onClick={handleLogout}
                className="ml-2 p-2 text-trc-gray-600 hover:text-red-600 transition-colors"
                title="Logout"
              >
                <LogoutIcon className="w-5 h-5" />
              </button>
            </div>
          </div>
        </div>
      </div>

      {/* Main content */}
      <div className="lg:pl-64">
        {/* Top bar */}
        <div className="sticky top-0 z-30 bg-white border-b">
          <div className="flex items-center justify-between h-16 px-4">
            <button
              className="lg:hidden"
              onClick={() => setSidebarOpen(true)}
            >
              <MenuIcon className="w-6 h-6" />
            </button>

            <div className="flex items-center space-x-4 ml-auto">
              <button className="p-2 text-trc-gray-600 hover:text-trc-gray-900 relative">
                <BellIcon className="w-6 h-6" />
                <span className="absolute top-1 right-1 w-2 h-2 bg-red-500 rounded-full"></span>
              </button>
              <Link href="/" className="text-sm text-trc-blue-600 hover:text-trc-blue-700">
                Back to Main Site
              </Link>
            </div>
          </div>
        </div>

        {/* Page content */}
        <main className="p-4 lg:p-8">
          {children}
        </main>
      </div>
    </div>
  );
}

